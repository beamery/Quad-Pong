#include "Event.h"


////////////////////////////////////////////////////////////////////////////////
// Event data
////////////////////////////////////////////////////////////////////////////////
MousePositionEvtData::MousePositionEvtData(int x, int y) :
	IEventData(MOUSE_POSITION, "Mouse movement event"),
	x(x), y(y) {}


////////////////////////////////////////////////////////////////////////////////
// Event listener
////////////////////////////////////////////////////////////////////////////////
void MousePositionListener::processEvent(IEventData *e)
{
	switch (e->getEventType())
	{
	case MOUSE_POSITION:
		onMouseMove((MousePositionEvtData*)e);
	}


}
